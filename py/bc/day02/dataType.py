# Data Type

## String
string = "Hello"
print(str(type(string)) + " " + string[0])  # H

## f-string
name = "John"
age = 20
print(f"Hello, my name is {name} and I am {age} years old.")  # Hello, my name is John and I am 20 years old.



## Integer
integer = 123_456_789
print(str(type(integer)) + " " + str(integer))  # 123,456,789

## Float
float = 3.14159
print(str(type(float)) + " " + str(float))  # 3.14159

## Boolean
boolean = True
print(str(type(boolean)) + " " + str(boolean))  # True

print(integer / 10 + integer % 10)
